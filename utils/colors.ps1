function Write-Blue {
    param (
        [string]$text
    )
    write-Host $text -ForegroundColor Blue
}
function Write-Red {
    param (
        [string]$text
    )
    write-Host $text -ForegroundColor Red
}
function Write-Green {
    param (
        [string]$text
    )
    write-Host $text -ForegroundColor Green
}
function Write-Yellow {
    param (
        [string]$text
    )
    write-Host $text -ForegroundColor Yellow
}